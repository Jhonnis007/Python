# Faça um programa que leia a largura e a altura de uma parede em metros, calcule a sua área e a
#quantidade de tinda necessária para pintá-la, sabendo que cada litro de tinta, pinta uma área de 2m²

L = float(input('Informe a largura da parede: '))
A = float(input('Informe a altura da parede: '))
AR = (L * A)
print('A area da parede a ser pintada é de {} m², e voce irá precisar de {} litros de tinta'.format((AR), AR /2))




